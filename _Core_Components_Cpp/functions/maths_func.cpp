#include <iostream>
#include <cmath>
using namespace std;

double sqrt_number(int main_num)
{
    double result = sqrt(main_num);
    return result;
}

double sin_number(double main_num)
{
    double result = sin(main_num * 3.14159 / 180);
    return result;
}

double cos_number(double main_num)
{
    double result = cos(main_num * 3.14159 / 180);
    return result;
}

double tan_number(double main_num)
{
    double result = tan(main_num * 3.14159 / 180);
    return result;
}

double round_number(double main_num)
{
    double result = round(main_num);
    return result;
}

double log_number(double main_num)
{
    double result = log(main_num);
    return result;
}
